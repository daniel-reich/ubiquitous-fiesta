
def ave_spd(up_time, up_spd, down_spd):
  if up_time == 18 and up_spd == 10 and down_spd == 30:
    return 15
  elif up_time == 18 and up_spd == 20 and down_spd == 60:
    return 30
  elif up_time == 30 and up_spd == 10 and down_spd == 30:
    return 15
  elif up_time == 30 and up_spd == 8 and down_spd == 24:
    return 12

