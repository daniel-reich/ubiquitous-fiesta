
class Calculator:
  # Write methods to add(), subtract(), multiply() and divide()
  def add(self, num_1, num_2):
    num_1 = num_1
    num_2 = num_2
    return num_1 + num_2
    
  def subtract(self, num_1, num_2):
    num_1 = num_1
    num_2 = num_2
    return num_1 - num_2
    
  def multiply(self, num_1, num_2):
    num_1 = num_1
    num_2 = num_2
    return num_1 * num_2
    
  def divide(self, num_1, num_2):
    num_1 = num_1
    num_2 = num_2
    return num_1 / num_2

