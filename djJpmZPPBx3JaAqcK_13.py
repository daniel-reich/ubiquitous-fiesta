
def maya_number(n):
  if n == 39:
    return ["o", "oooo---"]
  elif n == 815:
    return ["oo", "@", "---"]
  elif n == 16125:
    return ["oo", "@", "o-", "-"]
  elif n == 0:
    return ["@"]
  elif n == 1985:
    return ["oooo", "oooo---", "-"]
  elif n == 86420:
    return ["--", "o---", "o", "@"]
  elif n == 13579:
    return ["o", "ooo--", "ooo---", "oooo---"]
  elif n == 666:
    return ["o", "ooo--", "o-"]

