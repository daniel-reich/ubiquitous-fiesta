
digits = [
    '111101101101111',
    '010110010010111',
    '111001111100111',
    '111001111001111',
    '101101111001001',
    '111100111001111',
    '100100111101111',
    '111001001001001',
    '111101111101111',
    '111101111001001',
    "000010000010000"
]
def to_bit_string(t):
    d = [int(t[0]), int(t[1]), 10, int(t[3]), int(t[4])]
    return "".join("{}0{}{}{}0{}".format(*[digits[d[j]][r * 3: (r + 1) * 3]
                                         for j in range(5)])
                   for r in range(5))

