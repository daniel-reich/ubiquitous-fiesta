
def within_triangle(p0,p1,p2,tr):
    t = abs((p0[0] * (p1[1] - p2[1]) + p1[0] * (p2[1] - p0[1]) + p2[0]
            * (p0[1] - p1[1])) / 2)
    t1 = abs((tr[0] * (p1[1] - p2[1]) + p1[0] * (p2[1] - tr[1]) + p2[0]
             * (tr[1] - p1[1])) / 2)
    t2 = abs((p0[0] * (tr[1] - p2[1]) + tr[0] * (p2[1] - p0[1]) + p2[0]
             * (p0[1] - tr[1])) / 2)
    t3 = abs((p0[0] * (p1[1] - tr[1]) + p1[0] * (tr[1] - p0[1]) + tr[0]
             * (p0[1] - p1[1])) / 2)
    return t == t1 + t2 + t3

