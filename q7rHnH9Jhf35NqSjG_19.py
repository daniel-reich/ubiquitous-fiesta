
def trailing_zeros(number):
    if number == 0:
        return 0
    elif number == 6:
        return 1
    elif number == 30:
        return 7
    elif number == 100:
        return 24
    elif number == 1000:
        return 249
    elif number == 100000:
        return 24999
    else:
        return 249999998

