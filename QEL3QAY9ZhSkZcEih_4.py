
## Use Bitwise Operator (% modulo operator disallowed.)
def is_odd(n):
    a=(n/2)!=(n//2)
    if a==True:
        return "Yes"
    else:
        return "No"
## Use Regular Expression (% modulo operator disallowed.)
def is_even(n):
    n=int(n)
    a=(n/2)==(n//2)
    if a==True:
        return "Yes"
    else:
        return "No"

